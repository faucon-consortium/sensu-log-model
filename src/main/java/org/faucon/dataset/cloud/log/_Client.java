package org.faucon.dataset.cloud.log;

import java.util.List;

public class _Client {

	private List<String> address = null;
	private List<Boolean> keepalives = null;
	private List<String> name = null;
	private List<Boolean> safe_mode = null;
	private List<List<String>> subscriptions = null;
	
	public List<String> getAddress() {
		return address;
	}
	public void setAddress(List<String> address) {
		this.address = address;
	}
	public List<Boolean> getKeepalives() {
		return keepalives;
	}
	public void setKeepalives(List<Boolean> keepalives) {
		this.keepalives = keepalives;
	}
	public List<String> getName() {
		return name;
	}
	public void setName(List<String> name) {
		this.name = name;
	}
	public List<Boolean> getSafe_mode() {
		return safe_mode;
	}
	public void setSafe_mode(List<Boolean> safe_mode) {
		this.safe_mode = safe_mode;
	}
	public List<List<String>> getSubscriptions() {
		return subscriptions;
	}
	public void setSubscriptions(List<List<String>> subscriptions) {
		this.subscriptions = subscriptions;
	}


	
	
}
